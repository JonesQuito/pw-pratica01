
function resizeDown(){
  divPrincipal = document.getElementById("biografia");
  divPrincipal.style.backgroundColor = 'rgba(0,0,0,1)';
  divPrincipal.style.margin = '30px 20%';
  divPrincipal.style.transition = '0.5s';
}

function resizeUp(){
  divPrincipal = document.getElementById("biografia");
  divPrincipal.style.backgroundColor = 'rgba(0,0,0,.5)';
  divPrincipal.style.margin = '10px 10%';
  divPrincipal.style.transition = '0.5s';
}


